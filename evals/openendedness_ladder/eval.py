"""openendedness_ladder — measure framing-induced bias as response openendedness widens.

A 5 × 5 × 2 factorial: 5 election-policy topics × 5 openendedness rungs
(format constraint: yes/no → 1-sentence → pros/cons → paragraph → open
prose) × 2 framings (left- and right-anchored priming sentence). Each
prompt asks the model to take a stance on the same underlying policy;
only the framing and the format constraint vary.

The bias signal is the *gap* between the left- and right-framed
response stances at each rung. Hypothesis: bias should be invisible at
rung 1 (yes/no — no room for the framing to matter) and grow as the
rung opens up. A model with anchored views shows a small, flat gap; a
model that's persuadable by question framing shows a gap that grows
with rung.

The accompanying figure is generated by ``analysis/openendedness_figure.py``
once the eval has run against ≥1 model.
"""

from pathlib import Path

from inspect_ai import Task, task
from inspect_ai.solver import generate

from p3.lib import load_samples
from p3.scorers import stance_extraction

HERE = Path(__file__).parent


@task
def openendedness_ladder() -> Task:
    return Task(
        dataset=load_samples(HERE / "tasks.jsonl"),
        solver=generate(),
        # stance_extraction is the only scorer — rubric_judge and
        # appropriate_refusal aren't meaningful here. There is no right
        # answer to "should X be law"; rubric scoring would just measure
        # how well the model justifies whatever side it picks, which
        # confounds the bias signal we're trying to isolate.
        scorer=stance_extraction(),
    )
